import random


# %%
money = 100


# %%
def up_or_down(choice,bet):
    print("Welcome to 7 up, 7 down")
    global money
    dice = random.randint(1,12)
    
    if choice.lower() == "up" :
        if dice > 6:
            print("Outcome is " + str(dice) + ".")
            money += bet
            print ("Your balance is " + str(money))
            
        elif dice < 7:
            print("Outcome is " + str(dice) + ".")
            money -= bet
            print ("Your balance is " + str(money))
        
    elif choice.lower() == "down" :
        if dice > 6:
            print("Outcome is " + str(dice) + ".")
            money -= bet
            print ("Your balance is " + str(money))
            
        elif dice < 7:
            print("Outcome is " + str(dice) + ".")
            money += bet
            print ("Your balance is " + str(money))
            
    else:
        print("Please select from 'up' or 'down'.")
        
        
    


# %%
up_or_down('up',10)


# %%
print(money)


